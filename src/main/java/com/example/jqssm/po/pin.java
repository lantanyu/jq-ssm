package com.example.jqssm.po;

public class pin {
    private int pinid;
    private int userid;
    private int shuoid;
    private String pin;
    private String pintime;
    private String zt;
    private String pinname;

    public int getPinid() {
        return pinid;
    }

    public void setPinid(int pinid) {
        this.pinid = pinid;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public int getShuoid() {
        return shuoid;
    }

    public void setShuoid(int shuoid) {
        this.shuoid = shuoid;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getPintime() {
        return pintime;
    }

    public void setPintime(String pintime) {
        this.pintime = pintime;
    }

    public String getZt() {
        return zt;
    }

    public void setZt(String zt) {
        this.zt = zt;
    }

    public String getPinname() {
        return pinname;
    }

    public void setPinname(String pinname) {
        this.pinname = pinname;
    }
}
